def main():
    # ---- input paths (edit if needed) ----
    #CSV_PATH = "2025.02.12-miniNir (solucoes compostas)-Global.csv"
    OUT_XLSX = "miniNIR__REGRESSION__OUTPUT.xlsx"
    OUT_DIR = "miniNIR_regression_outputs"
    os.makedirs(OUT_DIR, exist_ok=True)

    # ---- load data ----
    df = pd.read_csv('2025.02.12-miniNir (solucoes compostas)-Global.csv', sep=";", decimal=",", encoding="latin-1")

    led_cols = [f"LED{i}_avg" for i in range(1, 7)]
    targets = {
        "Albumina": "Conc Albumina",
        "Creatinina": "Conc Creatinina",
        "Ureia": "Conc Ureia"
    }

    # predictors
    X = df[led_cols].to_numpy(dtype=float)

    # hold-out split (same split for all targets to keep comparability)
    idx = np.arange(len(df))
    tr_idx, te_idx = train_test_split(
        idx, test_size=0.2, random_state=RANDOM_STATE, shuffle=True
    )
    X_train, X_test = X[tr_idx], X[te_idx]

    # For PCA plots, we use the same preprocessing as in pipelines (but fitted on train only)
    vis_pre = Pipeline([
        ("pre", SavGolSNV(window=5, poly=2, deriv=0)),
        ("scaler", StandardScaler())
    ])
    Xtr_vis = vis_pre.fit_transform(X_train)
    Xte_vis = vis_pre.transform(X_test)

    # Storage
    all_metrics_sheets = {}
    summary_rows = []

    for analyte, ycol in targets.items():
        y = df[ycol].to_numpy(dtype=float)
        y_train, y_test = y[tr_idx], y[te_idx]

        # --- nested CV for model choice (anti-overfit) ---
        df_nested, best_model_name = nested_cv_select_best(
            X_train, y_train, outer_splits=5, inner_splits=4,
            scoring="neg_root_mean_squared_error"
        )

        # --- final fit on train and eval on test ---
        best_pipe, best_params, m_tr, m_te, yhat_test = fit_best_final(
            X_train, y_train, X_test, y_test,
            best_model_name, inner_splits=4,
            scoring="neg_root_mean_squared_error"
        )

        # Save joblib
        bundle = {
            "task": f"regression_{analyte.lower()}",
            "pipeline": best_pipe,
            "feature_cols": led_cols,
            "target_col": ycol,
            "best_model_name": best_model_name,
            "best_params": best_params
        }
        joblib_path = os.path.join(OUT_DIR, f"miniNIR_{analyte}__best_model.joblib")
        joblib.dump(bundle, joblib_path)

        # Plots: PCA train/test with % (color by concentration)
        pc1, pc2 = plot_pca_train_test(
            Xtr_vis, y_train, Xte_vis, y_test,
            out_train_png=os.path.join(OUT_DIR, f"PCA_{analyte}__train.png"),
            out_test_png=os.path.join(OUT_DIR, f"PCA_{analyte}__test.png"),
            title_prefix=f"miniNIR PCA | {analyte}"
        )

        # Predicted vs actual (validation/test)
        plot_pred_vs_actual(
            y_test, yhat_test,
            out_png=os.path.join(OUT_DIR, f"Pred_vs_Actual_{analyte}__test.png"),
            title=f"Predicted vs Actual (TEST) | {analyte} | Best={best_model_name}"
        )

        # Build per-analyte metrics table
        # We include nested CV summary + final metrics
        best_nested_row = df_nested[df_nested["Model"] == best_model_name].iloc[0].to_dict()
        metrics_row = {
            "Analyte": analyte,
            "BestModel": best_model_name,
            "NestedCV_RMSE_mean": best_nested_row["NestedCV_RMSE_mean"],
            "NestedCV_RMSE_std": best_nested_row["NestedCV_RMSE_std"],
            "NestedCV_R2_mean": best_nested_row["NestedCV_R2_mean"],
            "NestedCV_R2_std": best_nested_row["NestedCV_R2_std"],
            "Train_R2": m_tr["R2"],
            "Train_RMSE": m_tr["RMSE"],
            "Train_MAE": m_tr["MAE"],
            "Test_R2": m_te["R2"],
            "Test_RMSE": m_te["RMSE"],
            "Test_MAE": m_te["MAE"],
            "PC1_pct": pc1,
            "PC2_pct": pc2,
            "Best_params": str(best_params),
            "Joblib": os.path.basename(joblib_path)
        }
        all_metrics_sheets[analyte] = (df_nested, metrics_row)

        # summary rows for template-like sheet: include all models (nested) and fill test only for best
        for _, r in df_nested.iterrows():
            summary_rows.append({
                "Analyte": analyte,
                "Model": r["Model"],
                "NestedCV_R2": r["NestedCV_R2_mean"],
                "NestedCV_RMSE": r["NestedCV_RMSE_mean"],
                "NestedCV_MAE": np.nan,  # not computed in nested stage (optional)
                "Test_R2": m_te["R2"] if r["Model"] == best_model_name else np.nan,
                "Test_RMSE": m_te["RMSE"] if r["Model"] == best_model_name else np.nan,
                "Test_MAE": m_te["MAE"] if r["Model"] == best_model_name else np.nan,
                "Best_params": str(best_params) if r["Model"] == best_model_name else ""
            })

    # Write Excel
    with pd.ExcelWriter(os.path.join(OUT_DIR, OUT_XLSX), engine="openpyxl") as xw:
        # Per analyte: nested model comparison + one-row final metrics
        for analyte, (df_nested, metrics_row) in all_metrics_sheets.items():
            df_nested.to_excel(xw, index=False, sheet_name=f"{analyte}_NestedCV")
            pd.DataFrame([metrics_row]).to_excel(xw, index=False, sheet_name=f"{analyte}_Final")

    # Add template-like sheet using openpyxl
    xlsx_path = os.path.join(OUT_DIR, OUT_XLSX)
    wb = openpyxl.load_workbook(xlsx_path)
    build_template_like_sheet(wb, "Tabela_formato_template", summary_rows)
    wb.save(xlsx_path)

    print("OK ✅")
    print("Outputs in:", OUT_DIR)
    print("Excel:", xlsx_path)


