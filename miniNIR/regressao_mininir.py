RANDOM_STATE = 42


# -------------------------
# Preprocess: SavGol + SNV per sample (row)
# -------------------------
class SavGolSNV(BaseEstimator, TransformerMixin):
    """
    Applies Savitzky–Golay across the 6-LED vector (per sample) + SNV per sample.
    With only 6 features, the window must be small and odd (typically 5).
    """
    def __init__(self, window=5, poly=2, deriv=0):
        self.window = window
        self.poly = poly
        self.deriv = deriv

    def fit(self, X, y=None):
        return self

    def transform(self, X):
        X = np.asarray(X, dtype=float)
        n_feat = X.shape[1]

        w = int(self.window)
        if w % 2 == 0:
            w += 1
        if w > n_feat:
            w = n_feat if n_feat % 2 == 1 else max(3, n_feat - 1)
        if w < 3:
            Xf = X.copy()
        else:
            # IMPORTANT: smooth along feature axis (axis=1)
            Xf = savgol_filter(
                X,
                window_length=w,
                polyorder=min(self.poly, w - 1),
                deriv=self.deriv,
                axis=1
            )

        # SNV per sample
        mu = Xf.mean(axis=1, keepdims=True)
        sd = Xf.std(axis=1, keepdims=True) + 1e-12
        return (Xf - mu) / sd


