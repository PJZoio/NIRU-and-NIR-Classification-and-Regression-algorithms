# Excel: template-like summary (similar to "MiniNIR output.xlsx")
# -------------------------
def build_template_like_sheet(wb, sheet_name, summary_rows, title="Inferência\n(sol compostas)"):
    """
    Creates a summary sheet with a layout similar to the provided MiniNIR output template:
      Row1: title at col G
      Row2: headers
      Next: blocks per analyte with rows for each algorithm (incl. best model + others)
    We include both NestedCV (training-like) and Holdout Test (validation-like) columns.
    """
    if sheet_name in wb.sheetnames:
        del wb[sheet_name]
    ws = wb.create_sheet(sheet_name)

    thin = Side(border_style="thin", color="999999")
    border = Border(left=thin, right=thin, top=thin, bottom=thin)
    header_fill = PatternFill("solid", fgColor="D9E1F2")
    bold = Font(bold=True)

    # Title
    ws["G1"].value = title
    ws["G1"].font = Font(bold=True)

    # Headers (row 2)
    headers = [
        "Analito", "Algoritmo regressão",
        "NestedCV R2", "NestedCV RMSE", "NestedCV MAE",
        "Validação R2", "Validação RMSE", "Validação MAE",
        "Best params"
    ]
    for j, h in enumerate(headers, start=1):
        cell = ws.cell(2, j)
        cell.value = h
        cell.font = bold
        cell.fill = header_fill
        cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
        cell.border = border

    r = 3
    for row in summary_rows:
        vals = [
            row["Analyte"],
            row["Model"],
            row.get("NestedCV_R2", np.nan),
            row.get("NestedCV_RMSE", np.nan),
            row.get("NestedCV_MAE", np.nan),
            row.get("Test_R2", np.nan),
            row.get("Test_RMSE", np.nan),
            row.get("Test_MAE", np.nan),
            row.get("Best_params", "")
        ]
        for j, v in enumerate(vals, start=1):
            c = ws.cell(r, j)
            c.value = v
            c.border = border
            c.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
        r += 1

    # Column widths
    widths = [14, 20, 12, 14, 12, 12, 14, 12, 45]
    for i, w in enumerate(widths, start=1):
        ws.column_dimensions[get_column_letter(i)].width = w

    return ws

