# Model registry (same spirit as your v3 snippet, but regularised & tuned with nested CV)
# -------------------------
def model_grids():
    """
    Returns dict of model_name -> (estimator, param_grid, uses_scaler)
    All models run inside a pipeline with: SavGolSNV -> StandardScaler (optional) -> model
    """
    grids = {}

    # PLS (linear latent variables)
    grids["PLS"] = (
        PLSRegression(),
        {"model__n_components": [1, 2, 3, 4, 5]},
        True
    )

    # SVR (RBF)
    grids["SVR_RBF"] = (
        SVR(kernel="rbf"),
        {"model__C": [0.5, 1, 5, 10],
         "model__gamma": ["scale", 0.5, 0.1, 0.01],
         "model__epsilon": [0.01, 0.05, 0.1]},
        True
    )

    # Ridge (strong baseline, good against overfitting)
    grids["Ridge"] = (
        Ridge(random_state=RANDOM_STATE),
        {"model__alpha": [0.1, 1.0, 10.0, 50.0, 100.0]},
        True
    )

    # Tree ensembles (controlled depth/leaf to reduce overfitting)
    grids["RandomForest"] = (
        RandomForestRegressor(random_state=RANDOM_STATE),
        {"model__n_estimators": [400, 800],
         "model__max_depth": [None, 5, 8, 12],
         "model__min_samples_leaf": [2, 5, 10],
         "model__max_features": ["sqrt", 0.6, 1.0]},
        False
    )

    grids["GradientBoosting"] = (
        GradientBoostingRegressor(random_state=RANDOM_STATE),
        {"model__n_estimators": [300, 600],
         "model__learning_rate": [0.03, 0.05, 0.1],
         "model__max_depth": [2, 3, 4],
         "model__subsample": [0.7, 0.9, 1.0]},
        False
    )

    return grids


# -------------------------
# Nested CV selection (mitigates overfitting)
# -------------------------
def nested_cv_select_best(X, y, outer_splits=5, inner_splits=4, scoring="neg_root_mean_squared_error"):
    """
    Nested CV:
      outer loop -> unbiased generalization estimate per model
      inner loop -> hyperparameter tuning
    Returns:
      - df_nested: per-model outer mean/std (RMSE, R2)
      - best_model_name: chosen by lowest outer RMSE (mean)
    """
    outer = KFold(n_splits=outer_splits, shuffle=True, random_state=RANDOM_STATE)

    rows = []
    for name, (est, grid, uses_scaler) in model_grids().items():
        outer_rmse = []
        outer_r2 = []

        for tr_idx, te_idx in outer.split(X):
            X_tr, X_te = X[tr_idx], X[te_idx]
            y_tr, y_te = y[tr_idx], y[te_idx]

            steps = [("pre", SavGolSNV(window=5, poly=2, deriv=0))]
            if uses_scaler:
                steps.append(("scaler", StandardScaler()))
            steps.append(("model", est))
            pipe = Pipeline(steps)

            inner = KFold(n_splits=inner_splits, shuffle=True, random_state=RANDOM_STATE)
            gs = GridSearchCV(pipe, grid, scoring=scoring, cv=inner, n_jobs=-1)
            gs.fit(X_tr, y_tr)
            best = gs.best_estimator_

            yhat = best.predict(X_te)
            outer_rmse.append(rmse(y_te, yhat))
            outer_r2.append(r2_score(y_te, yhat))

        rows.append({
            "Model": name,
            "NestedCV_RMSE_mean": float(np.mean(outer_rmse)),
            "NestedCV_RMSE_std": float(np.std(outer_rmse)),
            "NestedCV_R2_mean": float(np.mean(outer_r2)),
            "NestedCV_R2_std": float(np.std(outer_r2)),
        })

    df_nested = pd.DataFrame(rows).sort_values("NestedCV_RMSE_mean", ascending=True).reset_index(drop=True)
    best_model_name = df_nested.loc[0, "Model"]
    return df_nested, best_model_name


# -------------------------
# Final fit on hold-out split (train/test) using the chosen model
# -------------------------
def fit_best_final(X_train, y_train, X_test, y_test, best_model_name, inner_splits=4,
                   scoring="neg_root_mean_squared_error"):
    est, grid, uses_scaler = model_grids()[best_model_name]

    steps = [("pre", SavGolSNV(window=5, poly=2, deriv=0))]
    if uses_scaler:
        steps.append(("scaler", StandardScaler()))
    steps.append(("model", est))
    pipe = Pipeline(steps)

    inner = KFold(n_splits=inner_splits, shuffle=True, random_state=RANDOM_STATE)
    gs = GridSearchCV(pipe, grid, scoring=scoring, cv=inner, n_jobs=-1)
    gs.fit(X_train, y_train)
    best_pipe = gs.best_estimator_
    best_params = gs.best_params_

    yhat_tr = best_pipe.predict(X_train)
    yhat_te = best_pipe.predict(X_test)

    m_tr = reg_metrics(y_train, yhat_tr)
    m_te = reg_metrics(y_test, yhat_te)

    return best_pipe, best_params, m_tr, m_te, yhat_te


