def plot_pred_vs_actual(y_true, y_pred, out_png, title):
    fig, ax = plt.subplots(figsize=(6.5, 5.5))
    ax.scatter(y_true, y_pred, alpha=0.75, edgecolors="k")
    lo = min(np.min(y_true), np.min(y_pred))
    hi = max(np.max(y_true), np.max(y_pred))
    ax.plot([lo, hi], [lo, hi])
    ax.set_xlabel("Actual concentration")
    ax.set_ylabel("Predicted concentration")
    ax.set_title(title)
    ax.grid(True)
    fig.tight_layout()
    fig.savefig(out_png, dpi=200)
    plt.close(fig)


def plot_pca_train_test(X_train_proc, y_train, X_test_proc, y_test, out_train_png, out_test_png, title_prefix):
    pca = PCA(n_components=2, random_state=RANDOM_STATE)
    Ztr = pca.fit_transform(X_train_proc)
    Zte = pca.transform(X_test_proc)

    pc1 = pca.explained_variance_ratio_[0] * 100
    pc2 = pca.explained_variance_ratio_[1] * 100

    def _scatter(Z, y, out_png, suffix):
        fig, ax = plt.subplots(figsize=(7, 5.5))
        sc = ax.scatter(Z[:, 0], Z[:, 1], c=y, alpha=0.85)
        cb = fig.colorbar(sc, ax=ax)
        cb.set_label("Concentration")
        ax.set_title(f"{title_prefix} | {suffix}")
        ax.set_xlabel(f"PC1 ({pc1:.1f}%)")
        ax.set_ylabel(f"PC2 ({pc2:.1f}%)")
        ax.grid(True)
        fig.tight_layout()
        fig.savefig(out_png, dpi=200)
        plt.close(fig)

    _scatter(Ztr, y_train, out_train_png, "Train")
    _scatter(Zte, y_test, out_test_png, "Test")
    return float(pc1), float(pc2)


