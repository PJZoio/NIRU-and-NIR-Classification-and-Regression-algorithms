# Predict the target variable
predictions = best_model.predict(X_new_scaled)
new_data['Predicted_Conc'] = predictions

# Save predictions to a new CSV
new_data.to_csv("predicted_concentrations.csv", index=False)
print("Predictions saved to 'predicted_concentrations.csv'.")

